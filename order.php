<?php
/*冒号排序开始*/
$num=array(90,70,50,30,20,0,10,40,60,80);
$cnt=count($num);
//print_r($cnt);
for($i=0;$i<$cnt;$i++){
    //print_r($i);0123456789
    for($j=0;$j<$cnt-$i-1;$j++){
        //print_r($j);012345678012345670123456012345012340123012010
        if($num[$j]>$num[$j+1]){
            $temp=$num[$j];
            $num[$j]=$num[$j+1];
            $num[$j+1]=$temp;
        }
    }
}
print_r($num);
/*冒号排序结束*/

?>